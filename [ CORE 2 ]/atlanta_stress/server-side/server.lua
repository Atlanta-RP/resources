local Tunnel = module("vrp","lib/Tunnel")
local Proxy = module("vrp","lib/Proxy")
vRP = Proxy.getInterface("vRP")
vRPclient = Tunnel.getInterface("vRP")
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONNECTION
-----------------------------------------------------------------------------------------------------------------------------------------
cnVRP = {}
Tunnel.bindInterface("atlanta_stress",cnVRP)
vCLIENT = Tunnel.getInterface("atlanta_stress")

-----------------------------------------------------------------------------------------------------------------------------------------
-- Stress reduction
-----------------------------------------------------------------------------------------------------------------------------------------
function cnVRP.abaixarStress()
	local source = source
	local user_id = vRP.getUserId(source)
	if user_id then
		vRP.downgradeStress(user_id,1)
	end
end