local Tunnel = module("vrp","lib/Tunnel")
local Proxy = module("vrp","lib/Proxy")
vRP = Proxy.getInterface("vRP")
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONNECTION
-----------------------------------------------------------------------------------------------------------------------------------------
cnVRP = {}
Tunnel.bindInterface("atlanta_stress",cnVRP)
vSERVER = Tunnel.getInterface("atlanta_stress")

-----------------------------------------------------------------------------------------------------------------------------------------
-- Redução de Stress em Bares
-----------------------------------------------------------------------------------------------------------------------------------------
local bares = {
    { 116.69,-1285.65,28.27,"vanilla" },
    {-1829.18,-1195.05,19.19,"pearls"},
    {-556.11,285.47,82.18,"tequila"},
    {368.52,277.14,91.19,"galaxy"},
    {-445.33,270.5,83.03,"comedyclub"}

}
Citizen.CreateThread(function()
	while true do
		Citizen.Wait(5000)
		local ped = PlayerPedId()
        for k,v in pairs(bares) do
            if GetDistanceBetweenCoords(v[1],v[2],v[3], GetEntityCoords(ped)) < 15.0 then
				vSERVER.abaixarStress();
    		end
        end
    end
end)